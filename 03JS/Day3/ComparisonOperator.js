// Comparison Operator
let a1 = 1;
let a2 = 2;
console.log(a1 == a2);
console.log(a1 > a2);
console.log(a1 < a2);
console.log(a1 <= a2);
console.log(a1 >= a2);

let s = "2";
// type conversion hua ha string ka number ma
console.log(a2 == s);
console.log(a2 < s);
console.log(a2 > s);
console.log(a2 >= s);
console.log(a2 <= s);

