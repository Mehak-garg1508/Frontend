// Assignment operator
// 20 value is assigned to number num
let x = 20;
console.log(x); //20

// addition
x = x + 10;
console.log(x); //30
x += 10;
console.log(x); //40

// Subtraction
// x = x - 10
x -= 10;
console.log(x); //30

// Multiplication
// x = x * 10;
x *= 10;
console.log(x); //300

// Division
// x = x / 10;
x /= 10;
console.log(x); //30

// Modulus
// x = x % 10;
x %= 10;
console.log(x); //0
