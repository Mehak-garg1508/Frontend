// Logical Opeartors
let age = 18;
let money = 420;
// And Operator (&&)
// Gives true only when both conditions are true
console.log(age < 18 && money > 400);
console.log(age == 18 && money > 400);
// Or operator (||)
// Gives false only when both statements are false
console.log(age < 18 || money > 400);
console.log(age < 18 || money < 400);

// Not Operator (!)
console.log(!(age > 18));
console.log(!(money > 400));
