// Datatype

// Number
let num = 24;
console.log(num);
console.log(typeof num);
num = 98.56;
console.log(num);
console.log(typeof num);

// String
let str = "Hello World";
console.log(str);
console.log(typeof str);

// Boolean
let isAdult = false;
console.log(isAdult);
console.log(typeof isAdult);
isAdult = true;
console.log(isAdult);

// NULL
// Jan puch kar value na dena
let n = null;
console.log(n);
console.log(typeof n);

// Undefined
let age;
console.log(age);
console.log(typeof age);
